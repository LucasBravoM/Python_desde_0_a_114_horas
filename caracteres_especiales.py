#Programa: Ejemplo de caracteres especiales en Python

# Salto de linea \n
mnensaje1 = "Hola \nMundo"
print(mnensaje1)

# Tabulación \t
print("123412341234123412341234")
mensaje2 = "\t\tJuan Perez"
print(mensaje2)

# Comillas dentro de cadena
mensaje3 = "Ella dijo: \"Hola a todes\""
print(mensaje3)

# Barra invertida
Mensaje4 = "C:\\Usuarios\\Lucas\\Documentos"
print(Mensaje4)

# Cadenas crudas ( raw string) r
mensaje5 = r"C:\Usuarios\Lucas\Documentos"
print(mensaje5)
